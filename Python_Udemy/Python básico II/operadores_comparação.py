a = 10
b = 10 

#compara e indica se são iguais ou não - operador boolean - true or false
print(a == b) # igual a 
print(a != b) # diferente de 
print(a > b) # maior que
print(a < b) # menor que
print(a < b) # menor que
print(a >= b) # maior ou igual a
print(a <= b) # menor ou igual a

